function StringToBinary(input) {
    let binaryArray = [];

    for (const char of input) {
        const binary = BinaryArt(char).map(row => row.split(''));

        if (binaryArray.length === 0) {
            binaryArray = binary;
        } else {
            binaryArray = binaryArray.map((row, index) => row.concat(binary[index]));
        }
    }

    return binaryArray;
}
